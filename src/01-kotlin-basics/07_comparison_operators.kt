fun main() {
    val a = 10
    val b = 20

    val result1 = a > b // lebih besar dari
    println(result1)
    val result2 = a < b // lebih kecil dari
    println(result2)

    val c = 11
    val d = 11

    val result3 = c >= d // lebih besar sama degan
    println(result3)
    val result4 = c <= d // lebih kecil sama dengan
    println(result4)
    val result5 = c == d // sama dengan
    println(result5)
    val result6 = c != d // tidak sama dengan
    println(result6)

}