fun main() {
    // penjumlahan
    val result = 2 + 1
    println(result)

    // pengurangan
    val result2 = 4 - 1
    println(result2)

    // perkalian
    val result3 = 3 * 1
    println(result3)

    // pembagian
    val result4 = 6 / 2
    println(result4)

    // sisa pembagian
    val result5 = 7 % 4
    println(result5)

    // augmented assignment operator
    var total = 0
    val produk1 = 111
    total += produk1
    val produk2 = 222
    total += produk2
    println(total)

    // unary operator
    var jumlah = 2
    jumlah++
    println(jumlah)

    val boolean = true
    println(!boolean) // boolean kebalikan
}